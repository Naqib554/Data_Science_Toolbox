# By the key() you can retrieve all the keys from the dictionary.
numbers = {1: 'one', 2: 'two', 3: 'three'}
dictionaryKeys = numbers.keys()
print(dictionaryKeys)

# you can use the key() in for loop to iterate the all the dictionaryKeys.
numbers = {1: 'one', 2: 'two', 3: 'three'}
for key in numbers.keys():
    print(key)

