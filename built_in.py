# This demonstrates how you can utilize various built-in functions in Python 
# to perform different operations on data structures like lists.



# Built-in Scope
num_list = [1, 2, 3, 4, 5]

# Finding the maximum value in a list
max_value = max(num_list)
print("Maximum value:", max_value)

# Finding the minimum value in a list
min_value = min(num_list)
print("Minimum value:", min_value)

# Summing the elements of a list
sum_values = sum(num_list)
print("Sum of values:", sum_values)

# Checking if all elements of a list are True
all_true = all(num_list)
print("All elements are True:", all_true)

# Checking if any element of a list is True
any_true = any(num_list)
print("Any element is True:", any_true)


